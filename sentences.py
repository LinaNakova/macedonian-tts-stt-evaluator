def get_sentence(index: int) -> str:
    """Get a sentence by index (wraps around)."""
    return SENTENCES[index % len(SENTENCES)]


def get_random_sentence() -> str:
    """Get a random sentence."""
    return random.choice(SENTENCES)


def get_total() -> int:
    """Return total number of sentences."""
    return len(SENTENCES)
